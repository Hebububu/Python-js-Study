print('4/2 :', 4/2)

import math # 수학과 관련된 '모듈' 을 가져왔다.
print('math.sqrt(4): ', math.sqrt(4)) # .sqrt 제곱근 구하기
print('math.pow(4,2): ', math.pow(4,2)) # .pow 제곱하기

import random # 랜덤과 관련된 '모듈'을 가져왔다. 
print('random.random(): ', random.random()) # .random 랜덤 값을 출력하는 기능 

